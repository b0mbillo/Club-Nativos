  <h1 class="display-4">Misión</h1>
  <p class="lead">El club deportivo Nativos Apalapajawaa 
    busca la formación de deportistas íntegros que armonicen 
    la relación CUERPO, DEPORTE Y CULTURA como principios de desarrollo
     de las dimensiones socio-afectiva, cognitiva y física por lo cual,
      se parte por promover el gusto por la práctica deportiva de niños,
       niñas, jóvenes y adultos a través de entrenamientos programas,
        sistematizados y de calidad que le permitan al deportista llegar al alto rendimiento.</p>
  <hr class="my-4">
  
<br>

  <h1 class="display-4">Visión</h1>
  <p class="lead">Para el año 2020 el club deportivo Nativos Apalapajawaa se ve reconocido como club de alto rendimiento a nivel local, nacional e internacional, centrado en:
- La participación en diferentes escenarios deportivos de Bogotá.
- El fortalecimiento de los entrenamientos a través de la natación, el ciclismo y el gimnasio.
- Suscitar la progresión de los resultados alcanzados por nuestros deportistas en las diferentes categorías y competencias en el ámbito de la Liga.
</p>
  <hr class="my-4">
 <br>
  <h1 class="display-4">Objetivo General.</h1>
  <p class="lead">Promover a través de la práctica deportiva un estilo
     de vida basado en la disciplina, persistencia y dedicación, que permita
      el perfeccionamiento de los fundamentos técnicos, tácticos y psicológicos
       del patinaje; aportando a la formación de personas integras tanto a nivel 
       competitivo como personal, en su relación consigo mismo y su entorno.
</p>
  <hr class="my-4">

  <h1 class="display-4">Objetivo Especificos.</h1>
  <p class="lead">
  
      <li>Fomentar la práctica del patinaje, el aprovechamiento del tiempo libre y el buen uso de los escenarios deportivos.</li>
      <li> Integrar a las familias y la comunidad en general a través de las actividades promovidas por la escuela buscando desarrollar el sentido de pertenencia.</li>
      <li>Generar experiencias de vida en los deportistas que permitan la interacción con nuevas personas y el medio ambiente.</li>
      <li>  Formar deportista con madurez, disciplina y proyecciones claras hacia las metas que desean alcanzar.</li>
  </ol>
    </p>
  <hr class="my-4">

  

  <h1 class="display-4">Nuestro Club</h1>
  <p class="lead">
  Es una organización avalada por el Instituto Distrital de Recreación y 
  Deporte(IDRD) bajo la resolución N.º 184 del marzo 9 de 2015. También, está 
  afiliado a la Liga de Patinaje de Bogotá bajo la resolución N.º 020 del 6 de 
  abril del 2018 con el nombre de club Nativos Apalapajawaa en la modalidad de 
  patinaje de velocidad. Es un proyecto pedagógico sin distinción de raza, creencia, 
  sexo o condición social que tiene por base la enseñanza y orientación del deporte 
  como experiencia fundamental para la formación integral.

  </p>
